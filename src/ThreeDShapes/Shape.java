package ThreeDShapes;

public interface Shape {
    /**
     * This is an empty method which will be created in the classes that implement this interface
     * @return - The volume of the shape will be returned
     */
    public double getVolume();

    /**
     * This is an empty method which will be created in the classes that implement this interface
     * @return -  The surface area of the shape will be returned
     */
    public double getSurfaceArea();
}
